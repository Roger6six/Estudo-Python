# aula de operadores aritmeticos

adicao = 2+2
print('Adição', adicao)

subtracao = 3-2
print('Subtração', subtracao)

multi = 10 * 5
print('Multiplicacao', multi)

divisao = 10 / 2.5  # retorna sempre um float
print('Divisao', divisao)

divisao_inteira = 10 // 2.5  # Corta as casas decimais
print('Divisao inteira', divisao_inteira)

exponenciacao = 2 ** 10
print('Exponenciação', exponenciacao)

modulo = 55 % 2  # resto da divisao
print('Modulo', modulo)
